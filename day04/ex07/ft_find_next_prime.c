int		ft_find_next_prime(int nb)
{
	int i;

	i = 2;
	while (nb < i)
		nb++;
	while (nb > i)
	{
		while (nb % i != 0)
			i++;
		if (nb % i == 0 && nb != i)
		{
			nb++;
			i = 2;
		}
	}
	if (nb == i)
		return (nb);
	return (0);
}
